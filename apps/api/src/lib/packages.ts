// ─── HTML / CSS ────────────────────────────────────────────────────────────
function buildHtml(icons: IconInput[], style: string): PackageOutput {
  const rootDir = `icon-forge-html-${style}`;
  const files: PackageFile[] = [];
  const symbols: string[] = [];

  for (const icon of icons) {
    const slug = kebabCase(icon.name);
    files.push({
      path: `icons/${slug}.svg`,
      content: icon.svg,
    });
    // Extract inner content for sprite symbol.
    const inner = icon.svg.replace(/^[\s\S]*?<svg[^>]*>([\s\S]*?)<\/svg>\s*$/i, '$1');
    const vbMatch = icon.svg.match(/viewBox=["']([^"']+)["']/i);
    const viewBox = vbMatch ? vbMatch[1] : '0 0 24 24';
    symbols.push(`  <symbol id="if-${slug}" viewBox="${viewBox}">${inner}</symbol>`);
  }

  files.push({
    path: 'sprite.svg',
    content: `<svg xmlns="http://www.w3.org/2000/svg" style="display:none">\n${symbols.join('\n')}\n</svg>\n`,
  });

  files.push({
    path: 'index.html',
    content: `<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <title>Icon Forge — ${style}</title>
  <style>
    body { background: #09090b; color: #fafafa; font-family: system-ui; padding: 2rem; }
    .grid { display: grid; grid-template-columns: repeat(auto-fill, minmax(120px, 1fr)); gap: 1rem; }
    .card { display: flex; flex-direction: column; align-items: center; padding: 1rem; background: #18181b; border: 1px solid #3f3f46; border-radius: 0.5rem; }
    .card svg { width: 48px; height: 48px; color: #fafafa; }
    .name { font-size: 0.75rem; margin-top: 0.5rem; color: #a1a1aa; }
  </style>
</head>
<body>
  <h1>Icon Forge — ${style} (${icons.length} icons)</h1>
  <p>Use any icon via inline SVG, the <code>sprite.svg</code> sprite + &lt;use&gt;, or as a CSS background.</p>
  <div class="grid">
${icons.map((i) => `    <div class="card"><svg><use href="sprite.svg#if-${kebabCase(i.name)}" /></svg><div class="name">${i.name}</div></div>`).join('\n')}
  </div>
</body>
</html>
`,
  });

  files.push({
    path: 'README.md',
    content: `# ${rootDir}\n\nIcon Forge HTML/CSS icon set (style: \`${style}\`).\n\n## Files\n\n- \`icons/<name>.svg\` — one SVG file per icon\n- \`sprite.svg\` — combined sprite for \`<use href="sprite.svg#if-<name>">\`\n- \`index.html\` — preview gallery\n\n## Use\n\n### Inline\n\n\`\`\`html\n<img src="icons/${kebabCase(icons[0]?.name ?? 'example')}.svg" width="32" height="32" />\n\`\`\`\n\n### Sprite + \`<use>\`\n\n\`\`\`html\n<svg width="32" height="32"><use href="sprite.svg#if-${kebabCase(icons[0]?.name ?? 'example')}" /></svg>\n\`\`\`\n\n## Icons (${icons.length})\n\n${icons.map((i) => `- \`${kebabCase(i.name)}.svg\` → \`#if-${kebabCase(i.name)}\``).join('\n')}\n`,
  });

  return { rootDir, files, archiveName: `${rootDir}.zip` };
}

