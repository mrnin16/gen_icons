/**
 * Per-framework package builders. Each builder turns a list of icons (DB rows)
 * into a directory tree of files that can be zipped and shipped to a user as
 * a locally-installable package for their language/framework of choice.
 *
 * v1: packages are NOT published anywhere — the user downloads the zip,
 * extracts it, then `npm install ./icon-forge-react` (or pubspec local path,
 * or SwiftPM local path, or Gradle includeBuild) into their project.
 */
import {
  FRAMEWORK_BY_ID,
  type Framework,
  type FrameworkId,
} from '@iconforge/shared';
import { kebabCase, pascalCase, snakeCase } from '@iconforge/shared';

/**
 * Frameworks where the npm `npm install <tarball-url>` flow works.
 * For others (Flutter / SwiftUI / Kotlin), users still need the zip + local
 * path install — those ecosystems consume Git URLs, not arbitrary tarballs.
 */
export const NPM_INSTALLABLE: ReadonlySet<FrameworkId> = new Set<FrameworkId>([
  'react',
  'nextjs',
  'vue',
  'svelte',
  'angular',
  'react-native',
]);

export type IconInput = {
  name: string;
  svg: string;
};

export type PackageFile = { path: string; content: string };

export type PackageOutput = {
  rootDir: string; // top-level folder name inside the zip
  files: PackageFile[]; // every file relative to rootDir/
  archiveName: string; // suggested .zip filename
};

const PACKAGE_VERSION = '0.1.0';
const PACKAGE_LICENSE = 'MIT';

function bannerComment(label: string, style: string): string {
  return `// Generated by Icon Forge. Style: ${style}. License: ${PACKAGE_LICENSE}.\n// ${label}\n`;
}

// ─── React ────────────────────────────────────────────────────────────────
function buildReact(icons: IconInput[], style: string, opts: { withUseClient: boolean; nameSuffix: string; pkgName: string; description: string }): PackageOutput {
  const rootDir = opts.pkgName;
  const files: PackageFile[] = [];
  const exports: string[] = [];

  for (const icon of icons) {
    const compName = pascalCase(icon.name) + 'Icon';
    const filename = `${compName}.tsx`;
    const useClient = opts.withUseClient ? `'use client';\n\n` : '';
    files.push({
      path: `src/${filename}`,
      content: bannerComment(filename, style) + useClient +
        FRAMEWORK_BY_ID.react.generate({ name: icon.name, svg: icon.svg, size: 24 }),
    });
    exports.push(`export { default as ${compName} } from './${compName}';`);
  }

  files.push({
    path: 'src/index.ts',
    content: bannerComment('index.ts', style) + exports.join('\n') + '\n',
  });

  files.push({
    path: 'package.json',
    content: JSON.stringify(
      {
        name: opts.pkgName,
        version: PACKAGE_VERSION,
        description: opts.description,
        license: PACKAGE_LICENSE,
        main: './src/index.ts',
        types: './src/index.ts',
        files: ['src'],
        sideEffects: false,
        peerDependencies: { react: '>=18' },
        keywords: ['icons', 'react', 'svg', 'icon-forge', style],
      },
      null,
      2,
    ) + '\n',
  });

  files.push({
    path: 'README.md',
    content: `# ${opts.pkgName}\n\n${opts.description}\n\n## Install (locally)\n\n\`\`\`bash\nnpm install ./${opts.pkgName}\n# or: yarn add file:./${opts.pkgName}\n\`\`\`\n\n## Use\n\n\`\`\`tsx\nimport { ${pascalCase(icons[0]?.name ?? 'Example')}Icon } from '${opts.pkgName}';\n\nexport default function App() {\n  return <${pascalCase(icons[0]?.name ?? 'Example')}Icon size={32} />;\n}\n\`\`\`\n\nEach component accepts \`size\` (number, default 24) and \`className\`.\n\n## Style\n\n\`${style}\`\n\n## Icons (${icons.length})\n\n${icons.map((i) => `- \`${pascalCase(i.name)}Icon\``).join('\n')}\n`,
  });

  files.push({
    path: 'tsconfig.json',
    content: JSON.stringify(
      {
        compilerOptions: {
          target: 'ES2020',
          module: 'ESNext',
          moduleResolution: 'Bundler',
          jsx: 'react-jsx',
          strict: true,
          declaration: true,
          esModuleInterop: true,
          skipLibCheck: true,
        },
        include: ['src'],
      },
      null,
      2,
    ) + '\n',
  });

  return { rootDir, files, archiveName: `${rootDir}.zip` };
}

